## Activity

![bg cover brightness:0.3 left:40%](../assets/hero-news-2026-02-13-brimbank-tech-school-workshop.avif)

_in breakout rooms..._

- have one person share screen and use the model (i.e. the grid) you created earlier
- follow the instructions to "roll the dice" and write **at least three new sentences** with your language model
- if you like, you can share the roles (one person shares screen and drives the random sampling, one person writes down the sentences as they "come out" of the model, etc.)

_time: 10 minutes, followed by shareback_

