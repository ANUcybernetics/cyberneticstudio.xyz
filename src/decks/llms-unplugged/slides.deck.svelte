## Activity

![bg cover brightness:0.3 left:40%](../assets/hero-news-2026-02-13-brimbank-tech-school-workshop.avif)

_in breakout rooms..._

- have one person share screen
- open [cybernetics.anu.edu.au/lx/](https://cybernetics.anu.edu.au/lx/) and choose some training data (text)
- open the **My First Language Model** spreadsheet (artisanal, organic & macro-free!) and follow the instructions to fill out your grid using _at least_ five sentences training data

_time: 10 minutes, followed by shareback_

